    <h1>Política de Privacidade</h1>
    <p>Esta política de privacidade descreve como coletamos, usamos e protegemos as informações pessoais dos nossos usuários. Ao utilizar nossos serviços, você concorda com a coleta e o uso de suas informações pessoais de acordo com esta política.</p>

    <h2>1. Informações Coletadas</h2>
    <p>Coletamos as seguintes informações pessoais dos usuários:</p>
    <ul>
        <li>Nome: Para personalizar a comunicação e oferecer uma experiência mais personalizada.</li>
        <li>Endereço de e-mail: Para enviar informações relevantes sobre nossos produtos, serviços e atualizações.</li>
        <li>Informações de pagamento: Quando necessário, para processar transações e pagamentos seguros.</li>
        <li>Dados de uso: Coletamos informações sobre como você utiliza nossos serviços, incluindo páginas visitadas, tempo gasto, recursos acessados e outros dados de uso.</li>
    </ul>

    <h2>2. Uso das Informações</h2>
    <p>Utilizamos as informações pessoais coletadas para:</p>
    <ul>
        <li>Fornecer e melhorar nossos serviços: Utilizamos suas informações para oferecer um serviço eficiente, personalizado e relevante.</li>
        <li>Comunicação: Enviaremos informações sobre nossos produtos, serviços, promoções e atualizações, desde que você tenha dado consentimento prévio.</li>
        <li>Processamento de Pagamentos: Quando necessário, utilizamos suas informações de pagamento para processar transações de forma segura e eficiente.</li>
        <li>Análise e Pesquisa: Podemos utilizar os dados de uso para análise estatística, pesquisa de mercado e aprimoramento dos nossos serviços.</li>
    </ul>

    <h2>3. Compartilhamento de Informações</h2>
    <p>Não compartilhamos suas informações pessoais com terceiros, exceto quando necessário para fornecer nossos serviços ou quando exigido por lei.</p>

    <h2>4. Proteção de Dados</h2>
    <p>Adotamos medidas de segurança adequadas para proteger suas informações pessoais contra acesso não autorizado, alteração, divulgação ou destruição. Utilizamos práticas de segurança padrão do setor para proteger suas informações.</p>

    <h2>5. Cookies e Tecnologias Semelhantes</h2>
    <p>Utilizamos cookies e outras tecnologias semelhantes para melhorar a experiência do usuário, personalizar conteúdos e anúncios, além de analisar padrões de uso. Você pode optar por desativar os cookies nas configurações do seu navegador, mas isso pode afetar a funcionalidade do nosso site.</p>

    <h2>6. Acesso e Atualização das Informações</h2>
    <p>Você tem o direito de acessar, corrigir e excluir suas informações pessoais. Caso queira atualizar seus dados, entre em contato conosco através dos canais indicados nesta política.</p>

    <h2>7. Alterações na Política de Privacidade</h2>
    <p>Podemos atualizar esta política de privacidade periodicamente. Recomendamos que você revise regularmente para se manter informado sobre como protegemos suas informações pessoais.</p>

    <h2>8. Contato</h2>
    <p>Caso tenha dúvidas, preocupações ou solicitações relacionadas a esta política de privacidade, entre em contato conosco através dos canais indicados em nosso site.</p>

    <p>Esta política de privacidade é válida a partir da data de sua publicação e se aplica a todos os usuários dos nossos serviços.</p>
